    def initialize_git_repository(self) -> bool:
        """Initialize Git repository with proper configuration."""
        print("üì¶ Initializing Git repository...")

        # Check if already a git repository
        if (self.project_root / ".git").exists():
            print("‚ÑπÔ∏è  Git repository already initialized")
            return True

        try:
            # Initialize git repository
            subprocess.run(["git", "init"], cwd=self.project_root, check=True)
            print("‚úÖ Git repository initialized")

            # Set default branch to main
            subprocess.run(
                ["git", "branch", "-M", "main"], cwd=self.project_root, check=True
            )
            print("‚úÖ Default branch set to 'main'")

            # Configure Git settings if not already set
            try:
                subprocess.run(
                    ["git", "config", "user.name"],
                    cwd=self.project_root,
                    check=True,
                    capture_output=True,
                )
            except subprocess.CalledProcessError:
                if self.git_username:
                    subprocess.run(
                        ["git", "config", "user.name", self.git_username],
                        cwd=self.project_root,
                        check=True,
                    )
                    print(f"‚úÖ Git user.name set to {self.git_username}")

            return True

        except subprocess.CalledProcessError as e:
            print(f"‚ùå Failed to initialize Git repository: {e}")
            return False

